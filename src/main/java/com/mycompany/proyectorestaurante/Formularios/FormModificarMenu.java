    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed
        String nombre = nombrePlato.getText();
        String precio = precioPlato.getText();
        if (!nombre.isEmpty() && !precio.isEmpty()) {
            admin.agregarPlatoMenu(nombrePlato, precioPlato);
            nombrePlato.setText("");
            precioPlato.setText("");
            JOptionPane.showMessageDialog(null, "Menu actualizado correctamente.");
            menuText.setText(admin.getMenu().toString());
        } else if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre del plato.");
        } else if (precio.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el precio del plato.");
        }


    }//GEN-LAST:event_guardarButtonActionPerformed

