    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        String numero = numeroPlato.getText();
        if (!numero.isEmpty()) {
            JLabel mensaje = new JLabel("¿Seguro que desea eliminar el plato numero " + numeroPlato.getText() + "?");
            int respuesta = JOptionPane.showOptionDialog(null,
                    mensaje,
                    "Confirmación",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    new Object[]{"Sí", "No"},
                    "Sí");

            if (respuesta == JOptionPane.YES_OPTION) {
                // El usuario seleccionó "Sí"
                admin.eliminarPlatoMenu(numeroPlato);
                numeroPlato.setText("");
                menuText.setText(admin.getMenu().toString());
            } else if (respuesta == JOptionPane.NO_OPTION) {
                // El usuario seleccionó "No"
                //System.out.println("Seleccionaste No");
            } else if (respuesta == JOptionPane.CLOSED_OPTION) {
                // El usuario cerró el cuadro de diálogo sin seleccionar ninguna opción
                //System.out.println("Cerraste el cuadro de diálogo");
            }
        } else if (numero.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese el numero del plato.");
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed

