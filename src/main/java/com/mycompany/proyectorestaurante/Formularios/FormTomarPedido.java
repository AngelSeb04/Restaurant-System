    private void agregarPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarPlatoActionPerformed
        try {
            String aux = platoText.getText();
            if (!aux.isEmpty()) {
                int numeroPlato = Integer.parseInt(platoText.getText());
                ObjectInputStream ois = new ObjectInputStream(new FileInputStream("archivos\\menu.txt"));
                Menu menu = (Menu) ois.readObject();
                if (numeroPlato <= menu.getPlatos().size() && numeroPlato > 0) {
                    int i = 1;
                    for (Map.Entry<String, Double> entry : menu.getPlatos().entrySet()) {
                        if (i == numeroPlato) {
                            String plato = entry.getKey();
                            double precio = entry.getValue();
                            int cantidad = Integer.parseInt(cantidadBox.getSelectedItem().toString());
                            String linea = " " + plato + "   x" + cantidad + "    " + (precio * cantidad) + "\n";
                            String texto = "";
                            textoResumenPedido.add(linea);

                            resumenPedido.setText(obtenerTextoPedido());
                            //StringBuilder sb = new StringBuilder();
                            //sb.append(i + " " + plato + "   x" + cantidad + "    " + (precio*cantidad) + "\n");
                            //resumenPedido.append(i + plato + "   x" + cantidad + "  " + (precio * cantidad) + "\n");
                            double precioTotal = precio * cantidad;
                            precioTotal += pedido.getPrecioTotal();
                            totalPedidoText.setText(String.valueOf(precioTotal));

                            pedido.setPrecioTotal(precioTotal);
                            pedido.getPlatos().put(plato, cantidad);
                            platoText.setText("");
                            cantidadBox.setSelectedIndex(0);
                        }
                        i++;
                    }
                }
                ois.close();
            } else {
                JOptionPane.showMessageDialog(null, "Plato no seleccionado");
            }

        } catch (IOException | ClassNotFoundException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.toString());
        }

    }//GEN-LAST:event_agregarPlatoActionPerformed

    private void ingresarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarPedidoActionPerformed
        String nombreCli = nombreClienteText.getText();
        String aux = resumenPedido.getText();

        if (!nombreCli.isEmpty() && !aux.isEmpty()) {
            //FALTA CODIGO
            pedido.setResumenPedido(resumenPedido.getText());
            pedido.setNombreCliente(nombreCli);
            pedido.setFecha(new Date());
            try {
                File file = new File("archivos\\pedidosActuales.txt");
                if (file.length() != 0) {
                    ObjectInputStream ois = new ObjectInputStream(new FileInputStream(file));
                    pedidosDia = (PedidosEnCurso) ois.readObject();
                    pedidosDia.agregarPedido(pedido);
                } else {
                    pedidosDia.agregarPedido(pedido);
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.toString());
            }

            platoText.setText("");
            resumenPedido.setText("");
            nombreClienteText.setText("");
            cantidadBox.setSelectedIndex(0);

            //NO SE GUARDA EN EL REPOSITORIO XQ TODAVIA PUEDE CANCELARSE
            pedido.setPrecioTotal(0);

            JOptionPane.showMessageDialog(null, "Pedido en curso agregado correctamente.");

        } else if (nombreCli.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre del cliente.");
        } else if (aux.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregar plato al pedido.");
        }
    }//GEN-LAST:event_ingresarPedidoActionPerformed

