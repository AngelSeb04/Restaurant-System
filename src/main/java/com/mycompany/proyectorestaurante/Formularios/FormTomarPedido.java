    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        String numero = numeroEliminarPedido.getText();
        int numeroEntero = Integer.parseInt(numero);

        if (!numero.isEmpty() && numeroEntero > 0 && numeroEntero <= textoResumenPedido.size()) {
            textoResumenPedido.remove(numeroEntero-1);
            
            String plato = "";  
            int i = 1;
            for (Map.Entry<String, Integer> entry : pedido.getPlatos().entrySet()) {
                if (i == numeroEntero) {
                    plato = entry.getKey();
                    
                }
                i++;
            }
            pedido.getPlatos().remove(plato);
            resumenPedido.setText(obtenerTextoPedido());
            JOptionPane.showMessageDialog(null, "Plato eliminado del pedido correctamente.");
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese un numero valido");
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed

